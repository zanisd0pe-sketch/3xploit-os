function playReceive() {
  try {
    const ctx = getAudioCtx();
    [0, 0.08].forEach(t => {
      const osc = ctx.createOscillator();
      const gain = ctx.createGain();
      osc.connect(gain);
      gain.connect(ctx.destination);
      osc.frequency.setValueAtTime(600 + t * 1000, ctx.currentTime + t);
      gain.gain.setValueAtTime(0.08, ctx.currentTime + t);
      gain.gain.exponentialRampToValueAtTime(0.001, ctx.currentTime + t + 0.08);
      osc.start(ctx.currentTime + t);
      osc.stop(ctx.currentTime + t + 0.08);
    });
  } catch(e) {}
}

